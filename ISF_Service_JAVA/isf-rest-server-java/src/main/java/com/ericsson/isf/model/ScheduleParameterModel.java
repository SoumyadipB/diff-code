package com.ericsson.isf.model;

public class ScheduleParameterModel {
	private int parameterID;
	private String parameterName;
	private String parameterIDValue;
	private String parameterValue;
	public int getParameterID() {
		return parameterID;
	}
	public void setParameterID(int parameterID) {
		this.parameterID = parameterID;
	}
	public String getParameterName() {
		return parameterName;
	}
	public void setParameterName(String parameterName) {
		this.parameterName = parameterName;
	}
	public String getParameterIDValue() {
		return parameterIDValue;
	}
	public void setParameterIDValue(String parameterIDValue) {
		this.parameterIDValue = parameterIDValue;
	}
	public String getParameterValue() {
		return parameterValue;
	}
	public void setParameterValue(String parameterValue) {
		this.parameterValue = parameterValue;
	}

}
